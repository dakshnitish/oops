class Patient:
    def setId(self,id):
        self.id = id

    def getId(self,id):
        return self.id

    def setName(self,name):        
        self.name = name

    def getName(self,name):
        return self.name    

    def setSSN(self,ssn):
        self.ssn = ssn

    def getSSN(self,ssn):
        return self.ssn        